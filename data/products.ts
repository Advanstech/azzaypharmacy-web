export const getDistributedProducts = (suppliers: { id: string }[]) => {
  return AZZAY_PRODUCTS_RAW.map((p, i) => {
    const name = p.name.toLowerCase();
    let cat = 'Other';
    if (name.includes('tab') || name.includes('cap')) cat = 'Tablets/Capsules';
    else if (name.includes('susp') || name.includes('syrup')) cat = 'Liquids/Suspensions';
    else if (name.includes('inj')) cat = 'Injections';
    else if (name.includes('cream') || name.includes('ointment') || name.includes('gel')) cat = 'Topicals';
    else if (name.includes('drop')) cat = 'Drops';
    
    return {
      id: `ap-${i}`,
      name: p.name,
      qty: p.qty,
      unit: p.unit,
      cost: p.cost,
      price: p.cost * 1.5,
      cat,
      supplierId: suppliers[i % suppliers.length].id,
    };
  });
};
